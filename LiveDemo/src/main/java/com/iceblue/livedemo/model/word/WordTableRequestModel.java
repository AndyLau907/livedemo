package com.iceblue.livedemo.model.word;

public class WordTableRequestModel {
    private String headerBackColor;
    private String rowBackColor;
    private String alternationBackColor;
    private String borderColor;

    public String getHeaderBackColor() {
        return headerBackColor;
    }

    public void setHeaderBackColor(String headerBackColor) {
        this.headerBackColor = headerBackColor;
    }

    public String getRowBackColor() {
        return rowBackColor;
    }

    public void setRowBackColor(String rowBackColor) {
        this.rowBackColor = rowBackColor;
    }

    public String getAlternationBackColor() {
        return alternationBackColor;
    }

    public void setAlternationBackColor(String alternationBackColor) {
        this.alternationBackColor = alternationBackColor;
    }

    public String getBorderColor() {
        return borderColor;
    }

    public void setBorderColor(String borderColor) {
        this.borderColor = borderColor;
    }
}
